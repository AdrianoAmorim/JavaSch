    private void btnBuscarPacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarPacienteMouseClicked
        if (tfBuscarPaciente.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(null, "Campo de Pesquisa em Branco.");
        } else {
            for (int i = 0; i < Dados.getPaciente().size(); i++) {
                if (Dados.getPaciente().get(i).getNome().equalsIgnoreCase(tfBuscarPaciente.getText())) {
                    //tfNomePaciente.setEnabled(true);
                    tfNomePaciente.setText(Dados.getPaciente().get(i).getNome());
                    tfBuscarPaciente.setText("");
                    tfNumConsulta.requestFocus();
                    break;
                } else if (i == Dados.getPaciente().size() - 1) {
                    JOptionPane.showMessageDialog(this, "Paciente Nao Cadastrado.", "AVISO", 0);
                    tfBuscarPaciente.setText("");
                    tfBuscarPaciente.requestFocus();
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnBuscarPacienteMouseClicked

    private boolean validarFormulario() {
        // testa os campos do formulario
        if (tfNumConsulta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O preenchimento do número da consulta é obrigatório!", "ATENÇÃO", 2);
            return false;
        } else if (tfNomePaciente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O preenchimento do nome do paciente é obrigatório!", "ATENÇÃO", 2);
            return false;
        } else if (tfData.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O preenchimento da data é obrigatório!", "ATENÇÃO", 2);
            return false;
        } else if (tfHorario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O preenchimento do horário da consulta é obrigatório!", "ATENÇÃO", 2);
            return false;
        }
        // se todos estiverem preenchidos, retorna verdadeiro
        return true;
    }


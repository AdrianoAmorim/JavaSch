    @SuppressWarnings("empty-statement")
    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked
        Consulta consulta = new Consulta();

        try {
            // checa todos os campos foram preenchidos
            if (validarFormulario() == true) {
                // passagem de valores dos textfields para o objeto
                consulta.setNumeroConsulta(Integer.parseInt(tfNumConsulta.getText()));
                consulta.setMedico(cbMedicos.getSelectedItem().toString());
                consulta.setPaciente(tfNomePaciente.getText());
                consulta.setData((tfData.getText()));
                consulta.setHorario(tfHorario.getText());
                // passagem de valores do objeto para o ArrayList
                Dados.setConsulta(consulta);
                JOptionPane.showMessageDialog(this, "Consulta Cadastrada com Sucesso.");
                // fecha a janela e retorna ao formulário principal
                this.dispose();
                principal.setVisible(true);
            }
        } catch (NumberFormatException erro_num_consulta) {
            JOptionPane.showMessageDialog(null, "O número da consulta é inválido!", "ERRO", 0);
        }
    }//GEN-LAST:event_btnCadastrarMouseClicked

